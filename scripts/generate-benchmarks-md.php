<?php

declare(strict_types=1);

/**
 * Generate docs/BENCHMARKS-LATEST.md from the most recent benchmark JSON.
 *
 * Usage:
 *   php scripts/generate-benchmarks-md.php
 *
 * Reads the most recent file from benchmarks/results/ (sorted alphabetically,
 * last = most recent by YYYY-MM-DD-sha naming convention).
 * Writes docs/BENCHMARKS-LATEST.md.
 */

$resultsDir = __DIR__ . '/../benchmarks/results';
$outputFile = __DIR__ . '/../docs/BENCHMARKS-LATEST.md';

if (!is_dir($resultsDir)) {
    fwrite(STDERR, "No benchmarks/results/ directory found. Run: php scripts/benchmark.php\n");
    exit(1);
}

$files = glob($resultsDir . '/*.json');
if (empty($files)) {
    fwrite(STDERR, "No JSON benchmark files found in {$resultsDir}\n");
    exit(1);
}

sort($files);
$latestFile = end($files);

$json = json_decode((string) file_get_contents($latestFile), true);
if (!is_array($json)) {
    fwrite(STDERR, "Failed to parse JSON from: {$latestFile}\n");
    exit(1);
}

$version = $json['version'] ?? 'unknown';
$sha = $json['git_sha'] ?? 'unknown';
$timestamp = $json['timestamp'] ?? 'unknown';
$env = $json['environment'] ?? [];
$runs = $json['runs'] ?? [];

$phpVersion = $env['php_version'] ?? 'unknown';
$os = $env['os'] ?? 'unknown';
$cpuModel = $env['cpu_model'] ?? 'unknown';
$cpuCores = $env['cpu_cores'] ?? '?';
$ramGb = $env['ram_gb'] ?? '?';

$lines = [];
$lines[] = '<!-- Auto-generated by `scripts/generate-benchmarks-md.php`. Run `php scripts/benchmark.php` to update. -->';
$lines[] = '';
$lines[] = '# Benchmarks';
$lines[] = '';
$lines[] = '> Auto-generated by `scripts/generate-benchmarks-md.php`. Run `php scripts/benchmark.php` to update.';
$lines[] = '';
$lines[] = "**Version:** {$version} (git: `{$sha}`)";
$lines[] = "**Generated:** {$timestamp}";
$lines[] = '';
$lines[] = '## Environment';
$lines[] = '';
$lines[] = "| Property | Value |";
$lines[] = "|----------|-------|";
$lines[] = "| PHP version | `{$phpVersion}` |";
$lines[] = "| OS | {$os} |";
$lines[] = "| CPU | {$cpuModel} |";
$lines[] = "| CPU cores | {$cpuCores} |";
$lines[] = "| RAM | {$ramGb} GB |";
$lines[] = '';
$lines[] = '## Results';
$lines[] = '';
$lines[] = '| Pages | Wall clock (s) | Pages/sec | Peak memory (MB) |';
$lines[] = '|-------|---------------|-----------|-----------------|';

foreach ($runs as $run) {
    $pages = $run['pages'];
    $time = number_format($run['wall_clock_seconds'], 3);
    $pps = number_format((float) $run['pages_per_second'], 0, '.', ',');
    $mem = number_format($run['peak_memory_mb'], 1);
    $lines[] = "| {$pages} | {$time} | {$pps} | {$mem} |";
}

$lines[] = '';
$lines[] = '## Raw runs (median-of-3)';
$lines[] = '';
$lines[] = '| Pages | Run 1 (s) | Run 2 (s) | Run 3 (s) | Median (s) |';
$lines[] = '|-------|-----------|-----------|-----------|------------|';

foreach ($runs as $run) {
    $pages = $run['pages'];
    $rawRuns = $run['raw_runs'] ?? [$run['wall_clock_seconds'], $run['wall_clock_seconds'], $run['wall_clock_seconds']];
    $r1 = number_format($rawRuns[0], 3);
    $r2 = number_format($rawRuns[1], 3);
    $r3 = number_format($rawRuns[2], 3);
    $median = number_format($run['wall_clock_seconds'], 3);
    $lines[] = "| {$pages} | {$r1} | {$r2} | {$r3} | {$median} |";
}

$lines[] = '';
$lines[] = '---';
$lines[] = '';
$lines[] = "Source file: `" . basename($latestFile) . "`";
$lines[] = '';

$content = implode("\n", $lines);
file_put_contents($outputFile, $content);

echo "Written: {$outputFile}\n";
echo "Source:  {$latestFile}\n";
